#include <minishell.h>

void	ms_exit(t_minishell *ms)
{
	int	j;

	j = 0;
	if ((ms->one_cmd)[1])
		j = ft_atoi(ms->one_cmd[1]);
	ms_free(ms, 0);
	ms->ret = j;
	exit(j);
}
